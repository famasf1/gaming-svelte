<script lang="ts">
	import { cn } from '$lib/utils';
	import type { HTMLAttributes } from 'svelte/elements';

	type Variant = 'default' | 'secondary' | 'destructive' | 'outline';

	interface Props extends HTMLAttributes<HTMLDivElement> {
		variant?: Variant;
		children?: any;
	}
	

	let { variant = 'default', class: className,children, ...props }: Props = $props();

	const variants: Record<Variant, string> = {
		default: 'border-transparent bg-primary text-primary-foreground hover:opacity-80',
		secondary: 'border-transparent bg-secondary text-secondary-foreground hover:opacity-80',
		destructive: 'border-transparent bg-destructive text-destructive-foreground hover:opacity-80',
		outline: 'text-foreground'
	};
</script>

<div
	class={cn(
		'inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:ring-2 focus:ring-ring focus:ring-offset-2 focus:outline-none',
		variants[variant],
		className
	)}
	{...props}
>
	{@render children?.()}
</div>
